package sample;


public class MyNode {
    private int indexX;
    private int indexY;
    private String string;

    public void setIndexX(int indexX) {
        this.indexX = indexX;
    }

    public void setIndexY(int indexY) {
        this.indexY = indexY;
    }

    public void setString(String string) {
        this.string = string;
    }

    public int getIndexX() {
        return indexX;
    }

    public int getIndexY() {
        return indexY;
    }

    public String getString() {
        return string;
    }
}
